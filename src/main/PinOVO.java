    private void btnPinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPinActionPerformed
        btnPin.setBackground(new java.awt.Color(57, 36, 117));
        
        try {
            sql = "SELECT o.pin_ovo AS PinOVO FROM ovo o" + ((action.matches("[0-9]+")) ? " WHERE o.no_ovo = ?" : " JOIN users u ON u.no_ovo = o.no_ovo WHERE u.username = ?");
            ps = con.prepareStatement(sql);
            ps.setString(1, action.matches("[0-9]+") ? action : userLogin);
            rs = ps.executeQuery();
            boolean valid = false;
            while(rs.next()) {
                if(rs.getString("PinOVO").equals(fieldPin.getText())) valid = true;
            }
            
            if(valid) {
                if(action.equals("delete")) {
                    sql = "UPDATE users SET no_ovo = ? WHERE username = ?";
                    ps = con.prepareStatement(sql);
                    ps.setString(1, "");
                    ps.setString(2, userLogin);
                    ps.executeUpdate();
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Nomor OVO berhasil diputuskan dari NikeShop!");
                    Profile pr = new Profile();
                    pr.setVisible(false);
                } else if(action.matches("[0-9]+")) {
                    sql = "UPDATE users SET no_ovo = ? WHERE username = ?";
                    ps = con.prepareStatement(sql);
                    ps.setString(1, action);
                    ps.setString(2, userLogin);
                    ps.executeUpdate();
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Nomor OVO berhasil disambungkan ke NikeShop!");
                    Profile pr = new Profile();
                    pr.setVisible(false);
                } else {
                    this.setVisible(false);
                    ConnectOVO co = new ConnectOVO();
                    co.connectOvo(userLogin);
                    co.setVisible(true);
                }
                
            } else {
                fieldPin.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                alertPin.setText("Pin OVO salah");
            }
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnPinActionPerformed

