    public void checkPin(String username, String actionTo) {
        userLogin = username;
        action = actionTo;
        
        if(action.equals("delete")) {
            btnPin.setText("PUTUSKAN");
        } else if(action.matches("[0-9]+")) {
            btnPin.setText("SAMBUNGKAN");
        }
    }
    
    private void checkField() {
        fieldPin.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {
                changed();
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                changed();
            }
            @Override
            public void insertUpdate(DocumentEvent e) {
                changed();
            }

            public void changed() {
                if(fieldPin.getText().equals("")) {
                     fieldPin.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                     alertPin.setText("Pin OVO harus diisi");
                 } else if(fieldPin.getText().length() < 6 || fieldPin.getText().length() > 6) {
                     fieldPin.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                     alertPin.setText("Pin OVO harus berisi 6 angka");
                 } else {
                     if((fieldPin.getText()).matches("[0-9]+")) {
                         alertPin.setText("");
                         fieldPin.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204, 204, 204)));

                     } else {
                         alertPin.setText("Pin OVO harus dalam bentuk angka");
                         fieldPin.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                     }
                 }
             }
        });
    }

