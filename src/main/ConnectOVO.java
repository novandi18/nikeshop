    private void btnNumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNumActionPerformed
        btnNum.setBackground(new java.awt.Color(57, 36, 117));
        
        try {
            sql = "SELECT no_ovo FROM ovo WHERE no_ovo = ?";
            ps = con.prepareStatement(sql);
            ps.setString(1, fieldNum.getText());
            rs = ps.executeQuery();
            boolean ovoExists = false;
            while(rs.next()) {
                ovoExists = true;
            }
            
            if(ovoExists) {
                sql = "SELECT o.no_ovo, u.no_ovo FROM ovo o JOIN users u ON u.no_ovo = o.no_ovo WHERE o.no_ovo = ?";
                ps = con.prepareStatement(sql);
                ps.setString(1, fieldNum.getText());
                rs = ps.executeQuery();
                boolean ovoUse = false;
                while(rs.next()) {
                    ovoUse = true;
                }
                
                if(ovoUse) {
                    fieldNum.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                    alertNum.setText("Nomor telepon sudah dipakai oleh pengguna lain di NikeShop");
                } else {
                    this.setVisible(false);
                    PinOVO pinO = new PinOVO();
                    pinO.checkPin(userLogin, fieldNum.getText());
                    pinO.setVisible(true);
                }
            } else {
                fieldNum.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                alertNum.setText("Nomor telepon tidak ditemukan");
            }
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnNumActionPerformed

