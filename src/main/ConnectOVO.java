    private void checkField() {
        fieldNum.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {
                changed();
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                changed();
            }
            @Override
            public void insertUpdate(DocumentEvent e) {
                changed();
            }

            public void changed() {
                if(fieldNum.getText().equals("")) {
                     fieldNum.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                     alertNum.setText("Nomor telepon OVO harus diisi");
                 } else if(fieldNum.getText().length() < 12) {
                     fieldNum.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                     alertNum.setText("Nomor telepon OVO minimal 12 angka");
                 } else {
                     if((fieldNum.getText()).matches("[0-9]+")) {
                         alertNum.setText("");
                         fieldNum.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204, 204, 204)));

                     } else {
                         alertNum.setText("Nomor telepon OVO harus dalam bentuk angka");
                         fieldNum.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(204,0,0)));
                     }
                 }
             }
        });
    }

